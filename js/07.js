// Operations order
let resultado = 10 + 10 * 5;
console.log(resultado); // 60

resultado = (10 + 10) * 5;
console.log(resultado); // 100


//Increment and decrement operators

let numero = 10;
numero++;
console.log(numero); // 11
console.log(++numero); // 12
console.log(numero++); // 12
console.log(numero); // 13

numero--;
console.log(numero); // 12
numero+=2;
console.log(numero); // 14

numero-=10;
console.log(numero); // 4